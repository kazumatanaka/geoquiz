// 対象のカードIDに一致する問題をランダムにn問取得する関数
function getUpgradeQuestions(cardId, n = 5) {
    // カードIDに対応する問題を抽出
    let pool = upgradeQuizData.filter(q => q.targetId === cardId);

    // シャッフルして返す
    //問題が足りない、または定義されていない場合は汎用問題（全範囲からランダム）を補充
    if (pool.length < n) {
        const generalPool = upgradeQuizData.filter(q => q.targetId !== cardId); // 自分以外から
        while (pool.length < n) {
            if (generalPool.length === 0) break;
            const randIndex = Math.floor(Math.random() * generalPool.length);
            pool.push(generalPool[randIndex]);
            generalPool.splice(randIndex, 1);
        }
    }
    return pool.sort(() => Math.random() - 0.5).slice(0, n);
}
